package org.decision;


//print odd/even
public class IfNine {

	public static void main(String[] args) {

		int p = 10;
		
		if(p>=60) {
			System.out.println("1");
		}
		else if(p>=50) {
			System.out.println("2");
		}
		else if(p>=40) {
			System.out.println("3");
		}
		else {
			System.out.println("failed");
		}
		
	}
}
