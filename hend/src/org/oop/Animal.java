package org.oop;

public class Animal {

	private String name;
	private String food;
	
	public void setName(String name) {
		this.name = name;
	}
	public void setFood(String food) {
		this.food = food;
	}
	
	public String getName() {
		return name;
	}
	public String getFood() {
		return food;
	}

}
